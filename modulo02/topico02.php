                        <h3 class="Subtopico">Conselho Estadual de Acesso à Informação (CEAI)</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">O CEAI é composto por seis membros (servidores) designado pelos Chefes dos Poderes Executivo, Legislativo e Judiciário bem como do Ministério Público, do Tribunal de Contas do Estado do Ceará e do Tribunal de Contas dos Municípios (art.5° da Lei 15.175/12). Vale ressaltar que, com o julgamento do STF da <strong>ADI n°5275/CE</strong>, ficou decidido pela inconstitucionalidade dos incisos que instituem o CEAI, restando apenas o membro do Poder Executivo. Não obstante essa decisão não tenha extinguido o citado conselho, o seu funcionamento restou inviabilizado. Assim, hoje não temos mais a segunda instância recursal em matéria de acesso à informação.</p>

                        <div id="subtopico02.2"></div>
                        <!-- Subtopico -->
                        <h3 class="Subtopico">Comitê Gestor de Acesso à Informação (CGAI)</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">O legislador estadual previu para cada Poder, no Ministério Público e nos Tribunais de Contas os Comitês Gestores de Acesso à Informação (CGAI). No caso específico do Poder executivo, a própria Lei n°15.175/12 (art 6°) previu que a sua coordenação seria exercida pela Controladoria e Ouvidoria Geral do Estado. Conforme já mencionado, possui a finalidade de deliberar sobre a classificação de informações sigilosas e apreciar os recursos interpostos. No âmbito do Poder Executivo o CGAI foi regulamentado por meio do Decreto Estadual n° 31.239/13.</p>

                        <div id="subtopico02.3"></div>

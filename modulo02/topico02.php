                        <h3 class="Subtopico">Comitê Setorial de Acesso à Informação (CSAI)</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">O Comitê Setorial de Acesso à Informação (CSAI) tem natureza colegiada, consultiva, propositiva, deliberativa e de caráter permanente, possui a finalidade de assegurar o acesso imediato à informação disponível e propor ao CGAI a classificação de informações no seu âmbito de atuação, devendo reunir-se ordinariamente <strong>uma vez por mês</strong>, em datas preestabelecidas, e extraordinariamente quando convocado pelo seu coordenador (Art. 1° e Art. 7° do Decreto Estadual n° 31.199/2013).</p>  
                        
                        <p class="wow fadeIn" data-wow-delay="0.3s">Segundo a legislação estadual (Art. 6° do Decreto Estadual n° 31.199/2013) deve ter um Comitê Setorial de Acesso à Informação (CSAI) em cada órgão ou entidade do Poder Executivo. A sua designação se dá mediante portaria da autoridade competente, indicando expressamente a função de cada membro, quais sejam:</p>


                        <p class="wow fadeIn" data-wow-delay="0.3s">O tema ganhou grande destaque principalmente em decorrência do avanço da tecnologia e consequentemente da utilização da internet e redes sociais. Desde a promulgação da nossa Constituição Federal de 1988, já estava previsto o direito da inviolabilidade da intimidade e da vida privada, do sigilo de correspondência e das comunicações telegráficas, de dados e das comunicações telefônicas (Art. 5, X e XII, CF/88).</p>

                        <p class="wow fadeIn" data-wow-delay="0.3s">Ante o exposto, e considerando a relevância da matéria, é necessário salientar que informações pessoais terão seu acesso restrito, independentemente de classificação de sigilo, pelo prazo máximo de 100 (cem) anos a contar da sua data de produção, a agentes públicos legalmente autorizados e à pessoa a que elas se referirem.</p>
                        

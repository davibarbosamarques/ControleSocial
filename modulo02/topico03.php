                        <h3 class="Subtopico">Informação Pública Versos Informação Pessoal</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">Não obstante o conceito de informação pública, acima apresentado, há situações em que podem suscitar dúvidas quanto à natureza pública da informação. Aqui, faremos um contraponto entre a informação pública e a de natureza pessoal.</p>
                        
                        <!-- Destaque verde -->
                        <div class="content_box_padrao">
                            <p class="wow fadeIn" data-wow-delay="0.3s">
                                <strong>A Lei Estadual n° 15.175/12 definiu em seu art. 3°, inciso I,</strong> o conceito de Informação de Interesse Público como sendo <strong>“toda aquela informação que não é de caráter pessoal ou classificada como sigilosa”. </strong>  Em seguida, no inciso III do mesmo dispositivo, o legislador estadual afirma que Informação Pessoal é <strong>“aquela relacionada à pessoa natural identificada ou identificável.”</strong> O cerne da questão está na definição do que vem a ser informação de caráter pessoal.
                            </p>
                        </div>

                        <p class="wow fadeIn" data-wow-delay="0.3s">Tendo em vista que a Lei de Acesso à Informação não definiu de forma clara o que seriam informações referentes à intimidade, à vida privada, à honra e à imagem do indivíduo deve-se buscar outros dispositivos para aclarar o entendimento sobre o assunto. O STJ, na sua resolução que regulamenta a Lei n. 12.527/2011 no Superior Tribunal de Justiça e dá outras providências, exemplifica o que seria informação pessoal, no artigo 3º, inciso V, senão vejamos:</p>


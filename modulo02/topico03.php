                        <h3 class="Subtopico">Classificação, Desclassificação e a Reavaliação de Informações Sigilosas.</h3>

                        <p class="wow fadeIn" data-wow-delay="0.3s">A noção de restrição de acesso à informação por meio de classificação encontra amparo na própria Constituição Federal, no inciso XXXIII do art. 5º, que eleva o direito de acesso à informação à categoria de direito fundamental. Ao disciplinar este dispositivo, a Lei de Acesso estabeleceu apenas nove hipóteses de restrição de acesso que representam atributos que podem ser identificados nas informações. Estão sujeitas à classificação, portanto, as informações que possam:</p>
                        <ul class="list">
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Pôr em risco a defesa e a soberania nacionais ou a integridade do território nacional; </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i>Prejudicar ou pôr em risco a condução de negociações ou as relações internacionais do País, ou as que tenham sido fornecidas em caráter sigiloso por outros Estados e organismos internacionais; </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Pôr em risco a vida, a segurança ou a saúde da população; </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Oferecer elevado risco à estabilidade financeira, econômica ou monetária do País; </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Prejudicar ou causar risco a planos ou operações estratégicos das Forças Armadas; </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Prejudicar ou causar risco a projetos de pesquisa e desenvolvimento científico ou tecnológico, assim como a sistemas, bens, instalações ou áreas de interesse estratégico nacional;</li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Pôr em risco a segurança de instituições ou de altas autoridades nacionais ou estrangeiras e seus familiares;</li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Comprometer atividades de inteligência, bem como de investigação ou fiscalização em andamento, relacionadas com a prevenção ou repressão de infrações; e 
                                </li>
                                <li class="wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-dot-circle text-success"></i> Colocar em risco a segurança do Presidente e Vice-Presidente da República e respectivos cônjuges e filhos(as).</li>
                            </ul>

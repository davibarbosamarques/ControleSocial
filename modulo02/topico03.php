                        <p class="wow fadeIn" data-wow-delay="0.3s">Por sua vez a LGPD, <strong>Lei n.º 13.709/2018</strong>, estabeleceu novas disposições acerca da matéria, a exemplo da definição de dado pessoal sensível (dado pessoal sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico, quando vinculado a uma pessoa natural) dentre outras. A LGPD aplica-se ao tratamento de dados pessoais, inclusive nos meios digitais, por pessoa natural ou pessoa jurídica de direito público ou privado, a partir dos seguintes fundamentos:</p>


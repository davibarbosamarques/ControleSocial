                    <p class="wow fadeIn" data-wow-delay="0.3s">Segundo a Comunidade da Nações Unidas (ONU), um governo é considerado bom e democrático se as instituições e os processos forem transparentes. Os processos realizados pelas instituições devem estar livres da corrupção e prestar contas à população. A ONU também afirma que para o um país alcançar o sucesso o padrão de governança é uma medida chave de credibilidade e respeito no mundo.</p>

                    <p class="wow fadeIn" data-wow-delay="0.3s">A boa governança é capaz de promover a igualdade, a participação, o pluralismo, a transparência, a responsabilidade e o Estado de Direito, de forma efetiva, eficiente e duradoura.</p>


                    <h3 class="Subtopico">Dados Abertos no Brasil</h3>

                    <p class="wow fadeIn" data-wow-delay="0.3s">A Lei de Acesso à Informação nacional, Lei 12.527/2011, trouxe a previsão de dados abertos governamentais, como uma das formas de promover a transparência ativa. Em seu artigo 8° traz as seguintes orientações:</p>


                    <p class="wow fadeIn" data-wow-delay="0.3s">O governo federal disponibiliza os seus dados abertos por meio do Portal Brasileiro de Dados abertos <a href="https://dados.gov.br/" target="_blank">dados.gov.br</a>. Nele estão contidos apenas dados abertos, mas das mais diversas áreas da administração pública, tais como dados da saúde suplementar, do sistema de transporte, de segurança pública, indicadores de educação, gastos governamentais, processo eleitoral, etc.</p>

                    <p class="wow fadeIn" data-wow-delay="0.3s">No entanto, importante mencionar que para a utilização dos dados abertos, é necessário um nível básico de conhecimento técnico. Para aprender como utilizar arquivos de dados, tais como CSV, JSON, XML e outros, o Portal de Dados Abertos indica o Manual do Desenvolvedor, do W3C Brasil, Escola de Dados, da <em>Open Knowledge</em>, ou diversos outros conteúdos que podem ser encontrados na internet.</p>

                    <p class="wow fadeIn" data-wow-delay="0.3s">Em 2016, foi publicado o Decreto Federal n.º 8.777/2016, que instituiu a Política de Dados Abertos do Poder Executivo Federal. Nele estão contidas algumas definições importantes como a de dados abertos e a de formato aberto:</p>


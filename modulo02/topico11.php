                    <h2 class="Titulo">Dados Abertos</h2>

                    <p class="wow fadeIn" data-wow-delay="0.3s">A definição de dados abertos, segundo a Open Knowledge Foundation – OKF, é:</p>

                    <!-- Destaque verde -->
                    <div class="content_box_padrao">
                        <p class="wow fadeIn" data-wow-delay="0.3s">
                        “Dados são abertos quando qualquer pessoa pode livremente usá-los, reutilizá-los e redistribuí-los, estando sujeito à, no máximo, exigência de creditar a autoria e compartilhar pela mesma licença”.
                        </p>
                    </div>

                    <p class="wow fadeIn" data-wow-delay="0.3s">Com a abertura de dados do governo, é possível o desenvolvimento de ferramentas cívicas, projetos, análises de políticas públicas, jornalismo de dados. Sendo possível de promover o conhecimento livre em diversos campos da sociedade, tornando a administração pública mais próxima do cidadão e transparente.</p>

                    <p class="wow fadeIn" data-wow-delay="0.3s">Com a disponibilização de dados abertos promovemos o conhecimento aberto que, segundo a Open Definition, o termo inclui:</p>

                    <!-- Lista -->
                    <div class="wow fadeIn col-sm-12 col-md-12" data-wow-delay="0.3s">
                        <ul class="card-style list-group list-group-flush">
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> Conteúdos como música, filmes, livros;</li>
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> Dados científicos, históricos, geográficos ou de outros tipos;</li>
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> Informação governamental e de administração. Segundo a Open Knowledge Foundation – OKF para um dado governamental ser considerado aberto deve ser:</li>
                        </ul>
                    </div><br>

                    <!-- Lista  -->
                    <ul class="list">
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Completo: todo o conteúdo disponível e não partes dele;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Primário: dados publicados na forma que está na fonte, sem alterações;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Atualizados: os dados devem ser publicados assim que gerados para preservar o seu valor;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Acessíveis: os dados devem estar disponíveis para o maior público possível, dessa forma devem estar disponíveis na internet;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i>  Processáveis por máquina: os dados devem estar razoavelmente estruturados para possibilitar o seu processamento automatizado, de forma a possibilitar a análise de grandes quantidades de registros sem intervenção manual.
                        </li>
                    </ul>

                    <!-- Lista -->
                    <div class="wow fadeIn col-sm-12 col-md-12" data-wow-delay="0.3s">
                        <ul class="card-style list-group list-group-flush">
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> Não é necessária a identificação para acessá-los: deve ser de livre acesso a todos, não sendo permitido nenhum tipo de cadastro para acessá-los;</li>
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> São disponibilizados em formatos não proprietários: os dados estão disponíveis em um formato sobre o qual nenhum ente detenha controle exclusivo;</li>
                            <li class="list-group-item"><i class="seta bi bi-arrow-right-short"></i> São livres de licença: os dados não estão sujeitos a regulações de direitos autorais, marcas, patentes ou segredo industrial. Sendo assim, não são considerados dados abertos:</li>
                        </ul>
                    </div><br>

                    <!-- Lista  -->
                    <ul class="list">
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Dados que não estão disponíveis na internet;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Dados que estão disponíveis na internet, porém em formatos proprietários, isto é, que necessitam de um software específico para acessá-los;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Dados em disponíveis em Portable Document Format (PDF) ou em formato de imagem, que não são facilmente processáveis por máquina;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i> Dados que, para serem acessados, requerem a identificação do interessado;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i>   Dados desatualizados;
                        </li>
                        <li class="wow fadeInLeft" data-wow-delay="0.3s">
                            <i class="fa fa-dot-circle text-success"></i>   Dados com restrições de licença, ou seja, que não podem ser livremente compartilhados.
                        </li>
                    </ul>
                    <p class="wow fadeIn" data-wow-delay="0.3s">Em nível mundial, vale destacar a atuação em dados abertos dos Estados Unidos e do Reino Unido que são consideradas por especialistas exemplos a serem seguidos por outras nações.</p>

                    <p class="wow fadeIn" data-wow-delay="0.3s">No <a href="https://www.data.gov/" target="_blank">data.gov</a> e no <a href="https://data.gov.uk/" target="_blank">data.gov.uk</a>, respectivamente, Estados Unidos e Inglaterra disponibilizam milhares de bases de dados em estado bruto – são 3.800 no site americano e 8.000 no britânico -, separam os dados por órgão governamental e têm ambos um eficiente mecanismo de busca, essencial num ambiente que trabalha com um enorme universo de dados.</p>

